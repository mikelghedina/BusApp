class Tickets:
    def __init__(self, sold, rest, total):
        self.sold = sold
        self.rest = rest
        self.total = total

    def getSold(self):
        return self.sold

    def setSold(self, sold):
        self.sold = sold

    def getRest(self):
        return self.rest

    def setRest(self, rest):
        self.rest = rest

    def getTotal(self):
        return self.total

    def setTotal(self, total):
        self.total = total




